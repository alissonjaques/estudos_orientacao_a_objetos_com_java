/**
	A classe Soma é uma biblioteca de métodos, cuja
	finalidade é a de somar números passados como 
	argumentos. Todos os seus métodos são estáticos e
	públicos, permitindo o acesso direto aos mesmos.
	
	@author Alisson Jaques

*/
public class Soma{ // declaração da classe
	
	/**
		O método somaDoisValores recebe como argumento
		dois valores inteiros e retorna a soma dos mesmos.
		@param a a primeira parcela inteira
		@param b a segunda parcela inteira
		@return a soma dos números informados
		
	*/
	public static int somaDoisValores(int a, int b){
	
		return a + b;		
		
	} // fim do método somaDoisValores
	
	/**
		O método sobrecarregado somaDoisValores recebe como 
		argumento dois valores flutuantes e retorna a soma dos
		mesmos.
		@param a a primeira parcela real
		@param b a segunda parcela real
		@return a soma dos números informados
		
	*/
	public static double somaDoisValores(double a, double b){
	
		return a + b;		
		
	} // fim do método sobrecarregado somaDoisValores
	
	/**
		O método somaTrêsValores recebe como argumento
		três valores inteiros e retorna a soma dos mesmos.
		@param a a primeira parcela inteira
		@param b a segunda parcela inteira
		@param c a terceira parcela inteira
		@return a soma dos números informados
		
	*/
	public static int somaTrêsValores(int a, int b, int c){
	
		return a + b + c;		
		
	} // fim do método somaTrêsValores
	
	/**
		O método sobrecarregado somaTrêsValores recebe como 
		argumento três valores flutuantes e retorna a soma 
		dos mesmos.
		@param a a primeira parcela real
		@param b a segunda parcela real
		@param c a terceira parcela real
		@return a soma dos números informados
		
	*/
	public static double somaTrêsValores(double a, double b, double c){
	
		return a + b + c;		
		
	} // fim do método sobrecarregado somaTrêsValores
	
	/**
		O método somaQuatroValores recebe como argumento
		quatro valores inteiros e retorna a soma dos mesmos.
		@param a a primeira parcela inteira
		@param b a segunda parcela inteira
		@param c a terceira parcela inteira
		@param d a quarta parcela inteira
		@return a soma dos números informados
		
	*/
	public static int somaQuatroValores(int a, int b, int c, int d){
	
		return a + b + c + d;		
		
	} // fim do método somaQuatroValores
	
	/**
		O método sobrecarregado somaQuatroValores recebe como 
		argumento quatro valores flutuantes e retorna a soma 
		dos mesmos.
		@param a a primeira parcela real
		@param b a segunda parcela real
		@param c a terceira parcela real
		@param d a quarta parcela real
		@return a soma dos números informados
		
	*/
	public static double somaQuatroValores(double a, double b, double c, double d){
	
		return a + b + c + d;		
		
	} // fim do método somaQuatroValores
	
	/**
		O método somaCincoValores recebe como argumento
		cinco valores inteiros e retorna a soma dos mesmos.
		@param a a primeira parcela inteira
		@param b a segunda parcela inteira
		@param c a terceira parcela inteira
		@param d a quarta parcela inteira
		@param e a quinta parcela inteira
		@return a soma dos números informados
		
	*/
	public static int somaCincoValores(int a, int b, int c, int d, int e){
	
		return a + b + c + d + e;		
		
	} // fim do método somaCincoValores
	
	/**
		O método sobrecarregado somaCincoValores recebe como 
		argumento cinco valores flutuantes e retorna a soma 
		dos mesmos.
		@param a a primeira parcela real
		@param b a segunda parcela real
		@param c a terceira parcela real
		@param d a quarta parcela real
		@param e a quinta parcela real
		@return a soma dos números informados
		
	*/
	public static double somaCincoValores(double a, double b, double c, double d, double e){
	
		return a + b + c + d + e;		
		
	} // fim do método somaCincoValores
	
} // fim da classe Soma