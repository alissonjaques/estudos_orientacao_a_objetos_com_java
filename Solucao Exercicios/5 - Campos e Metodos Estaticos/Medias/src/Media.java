/**
 *  A classe Media é uma biblioteca de métodos que possuí
 *  métodos para o cálculo de várias médias. Essa classe
 *  tem todos os seus métodos declarados como estáticos e 
 *  públicos, permintindo assim acesso direto aos mesmos.
 *  Ela pode ser extendida para o cálculo de mais médias.
 *  
 *  @author Alisson Jaques
 * 
 * 
 * */
public class Media { // declaração da classe
	
	/**
	 *  O método mediaDoisValores recebe como argumento
	 *  dois valores inteiros e retorna a média aritmética
	 *  dos mesmos.
	 *  @param a o primeiro número inteiro informado
	 *  @param b o segundo número inteiro informado
	 *  @return a média entre os números informados
	 *  
	 * */
	public static double mediaDoisValores(int a, int b) {
		
		double media = (a+b)/2;
		
		return media;
		
	} // fim do método mediaDoisValores
	
	/**
	 *  O método mediaDoisValores recebe como argumento
	 *  dois valores flutuantes e retorna a média aritmética
	 *  dos mesmos.
	 *  @param a o primeiro número flutuante informado
	 *  @param b o segundo número flutuante informado
	 *  @return a média entre os números informados
	 *  
	 * */
	public static double mediaDoisValores(double a, double b) {
		
		double media = (a+b)/2;
		
		return media;
		
	} // fim do método mediaDoisValores
	
	/**
	 *  O método mediaTrêsValores recebe como argumento
	 *  três valores inteiros e retorna a média aritmética
	 *  dos mesmos.
	 *  @param a o primeiro número inteiro informado
	 *  @param b o segundo número inteiro informado
	 *  @param c o terceiro número inteiro informado
	 *  @return a média entre os números informados
	 *  
	 * */
	public static double mediaTrêsValores(int a, int b, int c) {
		
		double media = (a+b+c)/3;
		
		return media;
		
	} // fim do método mediaTrêsValores
	
	/**
	 *  O método mediaTrêsValores recebe como argumento
	 *  três valores flutuantes e retorna a média aritmética
	 *  dos mesmos.
	 *  @param a o primeiro número flutuante informado
	 *  @param b o segundo número flutuante informado
	 *  @param c o terceiro número flutuante informado
	 *  @return a média entre os números informados
	 *  
	 * */
	public static double mediaTrêsValores(double a, double b, double c) {
		
		double media = (a+b+c)/3;
		
		return media;
		
	} // fim do método mediaTrêsValores
	
	/**
	 *  O método mediaQuatroValores recebe como argumento
	 *  quatro valores inteiros e retorna a média aritmética
	 *  dos mesmos.
	 *  @param a o primeiro número inteiro informado
	 *  @param b o segundo número inteiro informado
	 *  @param c o terceiro número inteiro informado
	 *  @param d o quarto número inteiro informado
	 *  @return a média entre os números informados
	 *  
	 * */
	public static double mediaQuatroValores(int a, int b, int c, int d) {
		
		double media = (a+b+c+d)/4;
		
		return media;
		
	} // fim do método mediaQuatroValores
	
	/**
	 *  O método mediaQuatroValores recebe como argumento
	 *  quatro valores flutuantes e retorna a média aritmética
	 *  dos mesmos.
	 *  @param a o primeiro número flutuante informado
	 *  @param b o segundo número flutuante informado
	 *  @param c o terceiro número flutuante informado
	 *  @param d o quarto número flutuante informado
	 *  @return a média entre os números informados
	 *  
	 * */
	public static double mediaQuatroValores(double a, double b, double c, double d) {
		
		double media = (a+b+c+d)/4;
		
		return media;
		
	} // fim do método mediaQuatroValores
	
	/**
	 *  O método mediaCincoValores recebe como argumento
	 *  cinco valores inteiros e retorna a média aritmética
	 *  dos mesmos.
	 *  @param a o primeiro número inteiro informado
	 *  @param b o segundo número inteiro informado
	 *  @param c o terceiro número inteiro informado
	 *  @param d o quarto número inteiro informado
	 *  @param e o quinto número inteitor informado
	 *  @return a média entre os números informados
	 *  
	 * */
	public static double mediaCincoValores(int a, int b, int c, int d, int e) {
		
		double media = (a+b+c+d+e)/5;
		
		return media;
		
	} // fim do método mediaCincoValores
	
	/**
	 *  O método mediaCincoValores recebe como argumento
	 *  cinco valores flutuantes e retorna a média aritmética
	 *  dos mesmos.
	 *  @param a o primeiro número flutuante informado
	 *  @param b o segundo número flutuante informado
	 *  @param c o terceiro número flutuante informado
	 *  @param d o quarto número flutuante informado
	 *  @param e o quinto número flutuante informado
	 *  @return a média entre os números informados
	 *  
	 * */
	public static double mediaCincoValores(double a, double b, double c, double d, double e) {
		
		double media = (a+b+c+d+e)/5;
		
		return media;
		
	} // fim do método mediaCincoValores
	
} // fim da classe Media